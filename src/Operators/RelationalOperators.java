package Operators;

public class RelationalOperators {
	
public static void main(String[] args) {
	        
	        int a = 10;
	        int b = 20;
	        
	        System.out.println("a = " + a + ", b = " + b);
	        System.out.println("-------------------------");

	        // Greater than
	        System.out.println("a > b  : " + (a > b));  

	        // Less than
	        System.out.println("a < b  : " + (a < b));  

	        // Greater than or equal to
	        System.out.println("a >= b : " + (a >= b)); 

	        // Less than or equal to
	        System.out.println("a <= b : " + (a <= b)); 

	        // Equal to
	        System.out.println("a == b : " + (a == b)); 

	        // Not equal to
	        System.out.println("a != b : " + (a != b)); 
	    }
	}



