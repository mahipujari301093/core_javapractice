package collection;

public class ArrayExample {
	
	public static void main(String[]args)
	{
		
		String names [] = {"Vishal","Radhika","Janavi","Madhuri"};
		
		int size =  names.length;
		
		System.out.println(size);
		
		
		for(int i= 0; i<size;i++)
		{
			
			System.out.println(names[i]);
		}
		
	}

}
