package collection;

public class Array2 {


    public static void main(String[] args) {

        int numbers[] = {10, 20, 30, 40, 50};

        int size = numbers.length;

        System.out.println("Array size is:" + size);

        for (int i = 0; i < size; i++)
        {
            System.out.println(numbers[i]);
        }


    }
}