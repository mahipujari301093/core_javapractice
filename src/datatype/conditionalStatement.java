package datatype;

public class conditionalStatement {
    public static void main(String[] args) {

        int i = 10;
        int j = 20;

        if (i == j)
        {
            System.out.println("True");
        }
        else
        {
            System.out.println("False");
        }

        if(i < j)
        {

            System.out.println("i is less than j");
        }
        else
        {
            System.out.println("i is not less than j");
        }
    }

}
